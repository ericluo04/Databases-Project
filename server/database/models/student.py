class Student():
    def __init__(self, id=None, first_name=None, last_name=None, year=None, college=None, major=None,
                 birth_month=None, birth_day=None, address=None, room=None, latitude=None, longitude=None):
        self.id = id
        self.first_name = first_name
        self.last_name = last_name
        self.year = year
        self.college = college
        self.major = major
        self.birth_month = birth_month
        self.birth_day = birth_day
        self.address = address
        self.room = room
        self.latitude = latitude
        self.longitude = longitude
